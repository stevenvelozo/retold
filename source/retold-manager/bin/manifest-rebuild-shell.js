#!/usr/bin/env node
/**
 * Regenerate modules/Include-Retold-Module-List.sh from the manifest.
 *
 *   node bin/manifest-rebuild-shell.js           # write the file
 *   node bin/manifest-rebuild-shell.js --check   # dry-run; exit 1 if drift
 *   node bin/manifest-rebuild-shell.js --print   # stdout only (for piping)
 *
 * Zero external dependencies. Reads Retold-Modules-Manifest.json, writes the
 * six `repositoriesXxx=(...)` bash arrays in the same layout the current file
 * uses. Preserves the shebang + the two `echo` wrapper lines so the shell
 * scripts that source this file (Checkout.sh, Status.sh, Update.sh) keep
 * printing their familiar banner.
 *
 * Module order within each group follows the manifest (which the backfill
 * script sorts alphabetically). If that's ever wrong, just edit the manifest
 * and re-run — the shell file becomes a pure derivation.
 */

const libFs = require('fs');
const libPath = require('path');

const REPO_ROOT = libPath.resolve(__dirname, '..', '..', '..');
const MANIFEST_PATH = libPath.join(REPO_ROOT, 'Retold-Modules-Manifest.json');
const SHELL_PATH = libPath.join(REPO_ROOT, 'modules', 'Include-Retold-Module-List.sh');

// Ordered list of (manifest group name → bash variable name). Controls the
// order of variable declarations in the generated file.
const GROUPS =
[
	{ ManifestName: 'Fable',   ShellVar: 'repositoriesFable' },
	{ ManifestName: 'Meadow',  ShellVar: 'repositoriesMeadow' },
	{ ManifestName: 'Orator',  ShellVar: 'repositoriesOrator' },
	{ ManifestName: 'Pict',    ShellVar: 'repositoriesPict' },
	{ ManifestName: 'Utility', ShellVar: 'repositoriesUtility' },
	{ ManifestName: 'Apps',    ShellVar: 'repositoriesApps' },
];

// ─────────────────────────────────────────────
//  Generator
// ─────────────────────────────────────────────

function loadManifest()
{
	let tmpRaw = libFs.readFileSync(MANIFEST_PATH, 'utf8');
	let tmpManifest = JSON.parse(tmpRaw);
	let tmpByGroup = {};
	for (let i = 0; i < tmpManifest.Groups.length; i++)
	{
		let tmpGroup = tmpManifest.Groups[i];
		tmpByGroup[tmpGroup.Name] = tmpGroup.Modules.map(function (pModule) { return pModule.Name; });
	}
	return tmpByGroup;
}

function renderShell(pByGroup)
{
	let tmpParts = [];
	tmpParts.push('#!/bin/bash');
	tmpParts.push('');
	tmpParts.push('# Generated by retold-manager/bin/manifest-rebuild-shell.js — do not hand-edit.');
	tmpParts.push('# Source of truth: Retold-Modules-Manifest.json at the repo root.');
	tmpParts.push('');
	tmpParts.push('echo "### Building list of modules..."');

	for (let i = 0; i < GROUPS.length; i++)
	{
		let tmpGroup = GROUPS[i];
		let tmpNames = pByGroup[tmpGroup.ManifestName] || [];
		let tmpQuoted = tmpNames.map(function (pN) { return '"' + pN + '"'; }).join(' ');
		tmpParts.push('');
		tmpParts.push(tmpGroup.ShellVar + '=(' + tmpQuoted + ')');
	}

	tmpParts.push('');
	tmpParts.push('echo "### ... Module lists built!"');
	tmpParts.push('');
	return tmpParts.join('\n');
}

function currentShell()
{
	try { return libFs.readFileSync(SHELL_PATH, 'utf8'); }
	catch (pError) { return null; }
}

function diffSummary(pCurrent, pDesired)
{
	if (pCurrent === pDesired)
	{
		return { Changed: false, Added: [], Removed: [] };
	}
	// Crude line-level diff — good enough for reporting which module names
	// would move in/out of the arrays.
	let tmpCurrentNames = extractNames(pCurrent || '');
	let tmpDesiredNames = extractNames(pDesired);
	let tmpAdded = [];
	let tmpRemoved = [];

	for (let tmpGroup of GROUPS)
	{
		let tmpCur = new Set(tmpCurrentNames[tmpGroup.ShellVar] || []);
		let tmpDes = new Set(tmpDesiredNames[tmpGroup.ShellVar] || []);
		for (let tmpName of tmpDes) { if (!tmpCur.has(tmpName)) { tmpAdded.push(tmpGroup.ShellVar + '/' + tmpName); } }
		for (let tmpName of tmpCur) { if (!tmpDes.has(tmpName)) { tmpRemoved.push(tmpGroup.ShellVar + '/' + tmpName); } }
	}
	return { Changed: true, Added: tmpAdded, Removed: tmpRemoved };
}

function extractNames(pShellText)
{
	let tmpResult = {};
	for (let tmpGroup of GROUPS)
	{
		let tmpRegex = new RegExp(tmpGroup.ShellVar + '=\\(([^\\)]*)\\)');
		let tmpMatch = pShellText.match(tmpRegex);
		if (!tmpMatch)
		{
			tmpResult[tmpGroup.ShellVar] = [];
			continue;
		}
		let tmpMatches = tmpMatch[1].match(/"([^"]+)"/g) || [];
		tmpResult[tmpGroup.ShellVar] = tmpMatches.map(function (pM) { return pM.slice(1, -1); });
	}
	return tmpResult;
}

// ─────────────────────────────────────────────
//  Atomic write (tmp + rename)
// ─────────────────────────────────────────────

function writeAtomic(pPath, pContent)
{
	let tmpTmp = pPath + '.tmp-' + process.pid + '-' + Date.now();
	let tmpFd = libFs.openSync(tmpTmp, 'w');
	try
	{
		libFs.writeSync(tmpFd, pContent, 0, 'utf8');
		libFs.fsyncSync(tmpFd);
	}
	finally
	{
		libFs.closeSync(tmpFd);
	}
	libFs.renameSync(tmpTmp, pPath);
}

// ─────────────────────────────────────────────
//  Main
// ─────────────────────────────────────────────

function main()
{
	let tmpArgs = process.argv.slice(2);
	let tmpCheck = tmpArgs.indexOf('--check') !== -1;
	let tmpPrint = tmpArgs.indexOf('--print') !== -1;

	let tmpByGroup = loadManifest();
	let tmpDesired = renderShell(tmpByGroup);

	if (tmpPrint)
	{
		process.stdout.write(tmpDesired);
		return;
	}

	let tmpCurrent = currentShell();
	let tmpDiff = diffSummary(tmpCurrent, tmpDesired);

	if (tmpCheck)
	{
		if (!tmpDiff.Changed)
		{
			console.log('✓ ' + SHELL_PATH + ' is in sync with the manifest.');
			process.exit(0);
		}
		console.log('! Drift detected. Run without --check to rebuild.');
		if (tmpDiff.Added.length)   { console.log('  Would add:    ' + tmpDiff.Added.join(', ')); }
		if (tmpDiff.Removed.length) { console.log('  Would remove: ' + tmpDiff.Removed.join(', ')); }
		process.exit(1);
	}

	if (!tmpDiff.Changed)
	{
		console.log('No changes needed — ' + SHELL_PATH + ' already matches the manifest.');
		return;
	}

	writeAtomic(SHELL_PATH, tmpDesired);
	console.log('✓ Wrote ' + SHELL_PATH);
	if (tmpDiff.Added.length)   { console.log('  Added:   ' + tmpDiff.Added.join(', ')); }
	if (tmpDiff.Removed.length) { console.log('  Removed: ' + tmpDiff.Removed.join(', ')); }
}

main();
